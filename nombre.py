# Asignar 4 variables cada una de un tipo diferente
entero = 42  # Tipo entero
flotante = 3.14  # Tipo flotante
cadena = "Hola, mundo"  # Tipo cadena
booleano = True  # Tipo booleano

# Imprimir el tipo y el valor de las variables creadas
print(f"entero: {entero}, tipo: {type(entero)}")
print(f"flotante: {flotante}, tipo: {type(flotante)}")
print(f"cadena: {cadena}, tipo: {type(cadena)}")
print(f"booleano: {booleano}, tipo: {type(booleano)}")